public class Elseif {
    public static void main(String[] args){
        int marks=78;
        if(marks>=90){
            System.out.println("Grade A");
        }else if(marks>=65){
            System.out.println("Grade B");
        }else if(marks>=50){
            System.out.println("Grade C");
        }
        else{
            System.out.println("Fail");
        }
    }
}
